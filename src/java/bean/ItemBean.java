package bean;

public class ItemBean {
    private String pn;
    private String description;
    private float size_h;
    private float size_w;
    private float size_d;
    private float weight;

    // <editor-fold desc="GET and SET" defaultstate="collapsed">
    public String getPn() {
        return pn;
    }

    public void setPn(String pn) {
        this.pn = pn;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public float getSize_h() {
        return size_h;
    }

    public void setSize_h(float size_h) {
        this.size_h = size_h;
    }

    public float getSize_w() {
        return size_w;
    }

    public void setSize_w(float size_w) {
        this.size_w = size_w;
    }

    public float getSize_d() {
        return size_d;
    }

    public void setSize_d(float size_d) {
        this.size_d = size_d;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }
    // </editor-fold>
}
